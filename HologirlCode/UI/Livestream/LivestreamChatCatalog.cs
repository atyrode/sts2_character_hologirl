    public static LivestreamChatLine GetReaction(LivestreamChatEvent eventKind)
    {
        var messages = EventMessages[eventKind];
        return CreateLine(PickMessage(messages));
    }

    public static IReadOnlyList<string>? GetPileOnTheme(LivestreamChatEvent eventKind)
    {
        if (!PileOnMessages.TryGetValue(eventKind, out var themes))
            return null;

        return themes[Random.Shared.Next(themes.Length)];
    }

    public static LivestreamChatLine GetPileOnReaction(IReadOnlyList<string> theme)
    {
        return CreateLine(PickMessage(theme));
    }

    public static LivestreamChatLine GetNeutral(LivestreamChatMood mood = LivestreamChatMood.Stable)
    {
        var roll = Random.Shared.NextSingle();
        if (roll < 0.025f)
            return CreateLine(PickMessage(CopypastaMessages));

        var messages = GetMoodMessages(mood, roll) ?? roll switch
        {
            < 0.18f => DirectHologirlMessages,
            < 0.54f => SocialMessages,
            _ => NeutralMessages
        };
        return CreateLine(PickMessage(messages));
    }

    public static LivestreamChatLine GetIdle(LivestreamChatMood mood = LivestreamChatMood.Stable)
    {
        if (Random.Shared.NextSingle() < 0.018f)
            return CreateLine(PickMessage(CopypastaMessages));

        var moodMessages = GetMoodMessages(mood, Random.Shared.NextSingle());
        return CreateLine(PickMessage(moodMessages ?? IdleMessages));
    }

    public static LivestreamChatLine GetReplyTo(LivestreamChatLine original)
    {
        var reply = PickMessage(ReplyMessages);
        return CreateLine($"@{original.Username} {reply}", original.Username);
    }

    public static IReadOnlyList<string> GetReplyPileOnTheme()
    {
        return ReplyPileOnMessages[Random.Shared.Next(ReplyPileOnMessages.Length)];
    }

    public static LivestreamChatLine GetReplyPileOnReaction(LivestreamChatLine original, IReadOnlyList<string> theme)
    {
        return CreateLine($"@{original.Username} {PickMessage(theme)}", original.Username);
    }

    private static string PickMessage(IReadOnlyList<string> messages)
    {
        var message = messages[Random.Shared.Next(messages.Count)];
        for (var attempt = 0; attempt < 12 && RecentMessageLookup.Contains(message); attempt++)
            message = messages[Random.Shared.Next(messages.Count)];

        RememberMessage(message);
        return message;
    }

    private static string[]? GetMoodMessages(LivestreamChatMood mood, float roll)
    {
        return mood switch
        {
            LivestreamChatMood.Critical when roll < 0.58f => CriticalMoodMessages,
            LivestreamChatMood.Tense when roll < 0.42f => TenseMoodMessages,
            LivestreamChatMood.Stable when roll < 0.28f => StableMoodMessages,
            _ => null
        };
    }

    private static void RememberMessage(string message)
    {
        if (!RecentMessageLookup.Add(message))
            return;

        RecentMessages.Enqueue(message);
        while (RecentMessages.Count > RecentMessageLimit)
            RecentMessageLookup.Remove(RecentMessages.Dequeue());
    }

    private static LivestreamChatLine CreateLine(string message, string? excludedUsername = null)
    {
        if (recurringChatters.Length == 0 && audienceProfile.RecurringChatterCount() > 0)
            SetAudienceProfile(audienceProfile);

        var useRecurringChatter = recurringChatters.Length > 0 &&
            Random.Shared.NextSingle() < audienceProfile.RecurringChatterChance();
        var roster = useRecurringChatter ? recurringChatters : Chatters;
        var chatter = roster[Random.Shared.Next(roster.Length)];
        for (var attempt = 0; attempt < 12 && IsBlockedChatter(chatter, useRecurringChatter, excludedUsername); attempt++)
            chatter = roster[Random.Shared.Next(roster.Length)];

        var username = useRecurringChatter ? $"*{chatter.Username}" : chatter.Username;
        lastUsername = username;

        return new LivestreamChatLine(
            username,
            chatter.Color,
            MaybeDecorateWithEmote(message));
    }

    private static bool IsBlockedChatter(LivestreamChatter chatter, bool recurring, string? excludedUsername)
    {
        var username = recurring ? $"*{chatter.Username}" : chatter.Username;
        return username == excludedUsername || chatter.Username == excludedUsername || username == lastUsername;
    }

    private static string MaybeDecorateWithEmote(string message)
    {
        if (message.StartsWith('@') || message.Length <= 7 || Random.Shared.NextSingle() >= 0.22f)
            return message;

        return $"{message} {EmoteCodes[Random.Shared.Next(EmoteCodes.Length)]}";
    }

